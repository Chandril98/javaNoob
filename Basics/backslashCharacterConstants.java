class backslash{
    public static void main(String args[]) {
        //used escape sequence to display text properly (quotes)
        System.out.println("There\'s vomit on his sweater already: Mom\'s spaghetti");
        //backspace
        System.out.println("Everybody\b knows the bird is the word");
        //newline
        System.out.println("This is a line\nThis is a new line!");
        //tab
        System.out.println("Keeping tabs like\tthat");
        //formfeed
        System.out.println("Noticed\fanything?");
        //carriage return
        System.out.println("What does it\rdo?");
        System.out.println("\n");
    }
}